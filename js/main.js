// Функция, которая принимает на вход два числа и возвращает их сумму:
function sum(num1, num2) {
  return num1 + num2;
}

console.log(sum(5, 7))

//Функция, которая принимает на вход строку и возвращает ее длину:
function stringLength(str) {
  return str.length;
}

console.log(stringLength("Hello, world!"));
